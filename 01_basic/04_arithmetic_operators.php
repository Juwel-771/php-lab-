<?php 

    // *** - Arithmetic operators are used to perform math operations on numbers in PHP.    
    // +	Addition	
    // -	Subtraction	
    // *	Multiplication	
    // /	Division	
    // %	Modulus
    // **	Exponentiation

    $a = 10;
    $b = 3;

    echo $a + $b;  // 13
    echo $a - $b;  // 7
    echo $a * $b;  // 30
    echo $a / $b;  // 3.333...
    echo $a % $b;  // 1
    echo $a ** $b; // 1000
    


?>