<?php 
    // *** -> A while loop is used to execute a block of code repeatedly as long as a specified condition remains true.

    $i = 1;

    while ($i <= 5) {
        echo "Number: " . $i . "<br>";
        $i++;  // Increment $i by 1 each time
    }

    // *** - output -> 
    // Number: 1
    // Number: 2
    // Number: 3
    // Number: 4
    // Number: 5

    

?>