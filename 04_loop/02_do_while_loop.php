<?php 
    // *** - A do-while loop is similar to a while loop, but it executes the block of code at least once before checking the condition.

    $i = 1;

    do {
        echo "Number: " . $i . "<br>";
        $i++;
    } while ($i <= 5);

    // *** - output
    // Number: 1
    // Number: 2
    // Number: 3
    // Number: 4
    // Number: 5


?>